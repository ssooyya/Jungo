package 입력.연습문제05;

public class Main {
    public static void main(String[] args) {
        System.out.println("전체 7자리로 맞추고 소수 4자리까지 출력");
        System.out.printf("x = %7.4f\n", 1.234);
        System.out.printf("y = %7.4f\n", 10.3459);
        System.out.println();
        System.out.println("소수 2자리까지 출력(반올림)");
        System.out.printf("x = %.2f\n", 1.234);
        System.out.printf("y = %.2f\n", 10.3459);
    }
}
