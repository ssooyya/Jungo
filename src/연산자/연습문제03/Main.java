package 연산자.연습문제03;

public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println("최초값 a = " + a + ", b = " + b);
        System.out.println();
        System.out.println("a++ = " + a++ + ", ++b = " + ++b);
        System.out.println("실행후 a = " + a + ", b = " + b);
        System.out.println();
        System.out.println("a-- = " + a-- + ", --b = " + --b);
        System.out.println("실행후 a = " + a + ", b = "+ b);
    }
}
